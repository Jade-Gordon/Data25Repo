"""
Numbers and math operators

T-shirts - 5 (int)
Cost per T-shirt - 5.50 (float)

Operator |      Meaning                                  |       Example             |
    +    |      Add                                      |      4 + 4 (8)            |
    -    |      Subtract                                |      4 - 2 (2)            |
    *    |      Multiply                                 |      3 * 4 (12)           |
    /    |      Divide                                   |      6 / 2 (3)            |
    %    |      Modulo - (divisible by) returns remainder|      5 % 2 (remainder = 1)|
"""

print(type(5))
print(type(5.50))
# Let's work out the total cost of a T-shirt order: 5 T-shirts that cost 5.50 each
print(5 * 5.50)    # this gives the answer: 27.5
"""
Now, let's look at the Modulo (%) symbol. 
It takes the number on the left, divides it by the number on the right, and then returns the remainder
"""
print(6 % 3)    # returns 0
print(6 % 4)    # returns 2


